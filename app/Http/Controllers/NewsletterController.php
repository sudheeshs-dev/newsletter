        $deleted=Newsletter::find($id)->delete();
        if($deleted){
            return redirect('newsletter')->with(['success'=>'Deleted Successfully']);
        }else{
            return redirect()->back()->with(['error'=>'Please Try Again']);
        }
    }
}
